public class for_loop{
    public static void main(String[] a1){
        for(int i=0; i<10; i++){
            System.out.println(i+ "th element is:"+ i);
        }

        for(int i=0; i<=10; i++){
            for(int j=0; j<=10; j++){
                System.out.println(i+"*"+j+"="+i*j);
            }
        }
    }
}

